    public void getDetails (){
        int birthid = Integer.parseInt(bid.getText());

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinemanagement","root","");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from booking where booking_id = '"+birthid+"'");

            if (rs.next()) {
                
                passid.setText(rs.getString(2));
                fid.setText(rs.getString(3));
                np.setText(rs.getString(4));
                pn.setText(rs.getString(5));
                add.setText(rs.getString(6));
                com.setText(rs.getString(7));
                cost.setText(rs.getString(8));
                status.setText(rs.getString(9));
            } else {
                add.setText("invalid  id");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
