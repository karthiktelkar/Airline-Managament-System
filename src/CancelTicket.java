     private boolean delete()
     {
            boolean isDeleted = false;
           try
           {
               
                String bookid=bid.getText();
                String flno=fid.getText();
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlinemanagement","root","");
                String sql="delete from booking where booking_id=? and flightid=?;";
                PreparedStatement del = con.prepareStatement(sql);
                del.setString(1,bookid);
                del.setString(2, flno);
                int rowCount = del.executeUpdate();
                 if (rowCount > 0){
                isDeleted = true;
            }else{
                isDeleted = false;
            }
            
            }catch (Exception e){
                e.printStackTrace();
            }
            return isDeleted;
     }
